package TNT_Bean;

public class AttendBean {
	int attendance_num;
	String student_id;
	String attendance_date;
	String entering_time;
	String leaving_time;
	String outgo_time;
	String return_time;
	String attendance_division;
	public int getAttendance_num() {
		return attendance_num;
	}
	public void setAttendance_num(int attendance_num) {
		this.attendance_num = attendance_num;
	}
	public String getStudent_id() {
		return student_id;
	}
	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}
	public String getAttendance_date() {
		return attendance_date;
	}
	public void setAttendance_date(String attendance_date) {
		this.attendance_date = attendance_date;
	}
	public String getEntering_time() {
		return entering_time;
	}
	public void setEntering_time(String entering_time) {
		this.entering_time = entering_time;
	}
	public String getLeaving_time() {
		return leaving_time;
	}
	public void setLeaving_time(String leaving_time) {
		this.leaving_time = leaving_time;
	}
	public String getOutgo_time() {
		return outgo_time;
	}
	public void setOutgo_time(String outgo_time) {
		this.outgo_time = outgo_time;
	}
	public String getReturn_time() {
		return return_time;
	}
	public void setReturn_time(String return_time) {
		this.return_time = return_time;
	}
	public String getAttendance_division() {
		return attendance_division;
	}
	public void setAttendance_division(String attendance_division) {
		this.attendance_division = attendance_division;
	}

}
